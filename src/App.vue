<script setup lang="ts">
import { ref } from 'vue'
import HelloWorld from './components/HelloWorld.vue'

interface Item {
  id: number
  texto: string
}

const vetor = ref<Item[]>([])
const texto = ref("")
let proximoId = 1

function adicionarVue() {
  if (texto.value.trim() === "") return

  vetor.value.push({
    id: proximoId++,
    texto: texto.value
  })

  texto.value = ""
}
</script>

<template>
  <header v-for="item in vetor" :key="item.id">
    <img
      alt="Vue logo"
      class="logo"
      src="./assets/logo.svg"
      width="125"
      height="125"
    />

    <div class="wrapper">
      <HelloWorld :msg="item.texto" />
    </div>
  </header>

  <main>
    <input
      v-model="texto"
      placeholder="Digite uma mensagem"
    />

    <button @click="adicionarVue">
      Novo Vue
    </button>
  </main>
</template>

